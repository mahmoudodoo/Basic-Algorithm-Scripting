
# Example: Using a for loop to iterate over a list
for i in range(5):
    print(f"Value of i: {i}")

# Example: Using an if-else conditional
x = 10
if x > 5:
    print("x is greater than 5")
else:
    print("x is not greater than 5")

# Example: Defining and using a function
def greet(name):
    return f"Hello, {name}!"

print(greet("Alice"))
